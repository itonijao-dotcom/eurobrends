const menuToggle = document.querySelector(".menu-toggle");
const siteNav = document.querySelector(".site-nav");
const navLinks = document.querySelectorAll(".site-nav a");
const statValues = document.querySelectorAll("[data-count]");
const langButtons = document.querySelectorAll(".lang-btn");
const metaDescription = document.querySelector('meta[name="description"]');
const mapFrame = document.querySelector(".location__map-shell iframe");

let currentLang = DEFAULT_LANG;

const t = (lang, key) => translations[lang]?.[key] ?? translations[DEFAULT_LANG]?.[key] ?? key;

const applyTranslations = (lang) => {
  currentLang = translations[lang] ? lang : DEFAULT_LANG;

  document.documentElement.lang = currentLang;
  document.title = t(currentLang, "meta.title");

  if (metaDescription) {
    metaDescription.setAttribute("content", t(currentLang, "meta.description"));
  }

  if (siteNav) {
    siteNav.setAttribute("aria-label", t(currentLang, "nav.aria"));
  }

  if (menuToggle) {
    menuToggle.setAttribute("aria-label", t(currentLang, "menu.toggle"));
  }

  if (mapFrame) {
    mapFrame.setAttribute("title", t(currentLang, "location.mapTitle"));
  }

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    element.textContent = t(currentLang, element.dataset.i18n);
  });

  document.querySelectorAll("[data-i18n-html]").forEach((element) => {
    element.innerHTML = t(currentLang, element.dataset.i18nHtml);
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
    element.setAttribute("placeholder", t(currentLang, element.dataset.i18nPlaceholder));
  });

  langButtons.forEach((button) => {
    button.classList.toggle("is-active", button.dataset.lang === currentLang);
  });

  const currentUrl = new URL(window.location.href);
  currentUrl.searchParams.set("lang", currentLang);
  window.history.replaceState({}, "", currentUrl);
  window.localStorage.setItem("eurobrends-language", currentLang);
};

window.addEventListener("load", () => {
  const urlLang = new URLSearchParams(window.location.search).get("lang");
  const storedLang = window.localStorage.getItem("eurobrends-language");
  applyTranslations(urlLang || storedLang || DEFAULT_LANG);

  if (window.location.hash) {
    const target = document.querySelector(window.location.hash);
    if (target) {
      target.scrollIntoView();
    }
  }
});

langButtons.forEach((button) => {
  button.addEventListener("click", () => {
    applyTranslations(button.dataset.lang);
  });
});

if (menuToggle && siteNav) {
  menuToggle.addEventListener("click", () => {
    const isOpen = menuToggle.classList.toggle("is-open");
    siteNav.classList.toggle("is-open", isOpen);
    menuToggle.setAttribute("aria-expanded", String(isOpen));
  });

  navLinks.forEach((link) => {
    link.addEventListener("click", () => {
      menuToggle.classList.remove("is-open");
      siteNav.classList.remove("is-open");
      menuToggle.setAttribute("aria-expanded", "false");
    });
  });
}

if (statValues.length) {
  const animateStat = (element) => {
    const total = Number(element.dataset.count);

    if (!total || element.dataset.animated === "true") {
      return;
    }

    element.dataset.animated = "true";

    const duration = 1400;
    const start = performance.now();

    const tick = (time) => {
      const progress = Math.min((time - start) / duration, 1);
      const current = Math.round(total * (1 - Math.pow(1 - progress, 3)));
      element.textContent = `${current}+`;

      if (progress < 1) {
        window.requestAnimationFrame(tick);
      }
    };

    window.requestAnimationFrame(tick);
  };

  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          animateStat(entry.target);
          observer.unobserve(entry.target);
        }
      });
    },
    { threshold: 0.6 }
  );

  statValues.forEach((value) => observer.observe(value));
}
